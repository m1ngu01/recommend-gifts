def main():
    collection = FIRESTORE_DB.collection("search_feedback")
    batch = FIRESTORE_DB.batch()
    now = datetime.utcnow()
    for entry in SURVEY_ENTRIES:
        doc_ref = collection.document()
        batch.set(
            doc_ref,
            {
                "search_log_id": None,
                "search_sentence": entry["search_sentence"],
                "answer": entry["answer"],
                "reason": entry["reason"],
                "user_email": "survey_seed@example.com",
                "status": "pending",
                "created_at": now,
            },
        )
    batch.commit()
    print(f"Inserted {len(SURVEY_ENTRIES)} survey feedback documents.")


if __name__ == "__main__":
    main()
