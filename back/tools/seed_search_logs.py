def main():
    batch = FIRESTORE_DB.batch()
    collection = FIRESTORE_DB.collection("search_logs")
    now = datetime.utcnow().isoformat()
    for sentence in SENTENCES:
        doc_ref = collection.document()
        batch.set(
            doc_ref,
            {
                "sentence": sentence,
                "user_email": "seed@example.com",
                "metadata": {"source": "seed_script", "seeded_at": now},
                "created_at": datetime.utcnow(),
                "annotated": False,
            },
        )
    batch.commit()
    print(f"Inserted {len(SENTENCES)} sample search logs.")


if __name__ == "__main__":
    main()
